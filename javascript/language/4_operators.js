// addition
console.log(1 + 1); // 2

// subtraction
console.log(1 - 1); // 0

// multiplication
console.log(2 * 2); // 4

// division
console.log(4 / 2); // 2

// modulo
console.log(5 % 2); // 1

// increment
var i = 1;
i++;
console.log(i); // 2
i+=1;
console.log(i); // 3
i=i+1;
console.log(i); // 4

// decrement
var i = 1;
i--;
console.log(i); // 0
i-=1;
console.log(i); // -1
i=i-1;
console.log(i); // -2

// comparison
console.log(1 === 1); // true
console.log(1 === 2); // false

// logical AND
console.log(true && true); // true
console.log(true && false); // false

// logical OR
console.log(true || true); // true
console.log(true || false); // true

// logical NOT
console.log(!true); // false
console.log(!false); // true

// == vs ===
console.log(1 == "1"); // true
console.log(1 === "1"); // false